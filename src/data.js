// ── Utility functions ───────────────────────────
export function getRandomAffirmation(allAffirmations = affirmations) {
  return allAffirmations[Math.floor(Math.random() * allAffirmations.length)];
}

export function getCompletedExams(examList = examDates) {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  return examList.filter((exam) => {
    const examDate = new Date(exam.date + 'T00:00:00');
    return today > examDate;
  });
}
